                                          ## IF ELSE STATEMENT ###

#IF  A GIVEN CONDITION INSIDE if WILL TRUE THEN AFTER STATEMENT WILL EXECUTE OTHER WISE NOT
#USE OF if  STATEMENT      [ SYNTAX OF  if :-    if(condition): ]
#FOR EXAMPLE
a=45          # VALUE OF a IS 45
b=25          # VALUE OF B IS 25
if(a>b):      # MEANS IF "a" IS GREATER THAN "b"  THEN AFTER INSIDE THE if  STATEMENT WILL EXECUTE
    print("a is greatest ")




#USE OF else STATEMENT
#IF if STATEMENT WILL NOT TRUE THEN ELSE STATEMENT WILL EXECUTE
#FOR EXAMPLE
c=-1
d=20
if(c>0):               # C IS MINUS 1 SO if STATEMENT WILL NOT EXECUTE
    print("c is greater than zero ")
else:                  # if STATEMENT IS NOT TRUE SO else STATEMENT WILL EXECUTE
    print("d is greater than zero")




